package exam;

public class Triangle {
    // 밑변, 높이
    private int baseLine;
    private int height;

    Triangle() {

    }

    public Triangle(int baseLine, int height) {
        this.baseLine = baseLine;
        this.height = height;
    }

    @Override
    public String toString() {
        return "Triangle [baseLine=" + baseLine + ", height=" + height + "]";
    }

    
}




































































